# Example to add elements

Employee = {'name': 'Kiryu', 'age': 17}
print(Employee)
# O/P: {'name': 'Kiryu', 'age': 17}

# Add new element (key: value) to existing dictionary
Employee['department'] = 'Software Engineer'
print(Employee)
# O/P: {'name': 'Kiryu', 'age': 17, 'department': 'Software Engineer'}

Employee['name'] = 'Muthukumer'
Employee['age'] = 50
print(Employee)
# O/P: {'name': 'Muthukumer', 'age': 50, 'department': 'Software Engineer'}