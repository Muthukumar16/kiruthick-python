# min()
tplNum = (10,12,14,20,22,24,30,32,34,0,0,0,0,0)
print(min(tplNum)) # O/P: 0

tplStr = ("Kiryu", "Zara", "Arasu")
print(min(tplStr)) # O/P: Arasu

# sum()
print(sum(tplNum)) # O/P: 198
# print(sum(tplStr)) # O/P: TypeError: unsupported operand type(s) for +: 'int' and 'str'

# max()
print(max(tplNum)) # O/P: 34
print(max(tplStr)) # O/P: Zara

# index()
print(tplNum.index(20)) # O/P: 3

# count()
print(tplNum.count(0)) # O/P: 5



